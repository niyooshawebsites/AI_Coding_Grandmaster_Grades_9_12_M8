# Storing personal details in a tuple
personal_details = (
    "Harsh",          # First Name
    "Sharma",         # Last Name
    15,               # Age
    5.7,              # Height (in feet)
    55,               # Weight (in kg)
    "Mathematics"     # Favourite Subject
)

print("Personal Details in Tuple:")
print(personal_details)

# Converting tuple to list
personal_details_list = list(personal_details)

print("\nPersonal Details after converting to List:")
print(personal_details_list)